// String#scan
if (!String.prototype.scan) {
  String.prototype.scan = function (regex) {
    if (!regex.global) throw "regex must have 'global' flag set";
    var str = this;
    var m;
    var result = [];
    while (m = regex.exec(str)) {
      m.shift();
      result.push(m);
    }
    return result;
  }
}

